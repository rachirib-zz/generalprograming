package co.com.fc.rules.dto;

public class AprobacionCreditoDTO {

	private Long edad;
	private Long acierta;
	private Long estadoCivil;
	private Long aprobado;
	public Long getEdad() {
		return edad;
	}
	public void setEdad(Long edad) {
		this.edad = edad;
	}
	public Long getAcierta() {
		return acierta;
	}
	public void setAcierta(Long acierta) {
		this.acierta = acierta;
	}
	public Long getEstadoCivil() {
		return estadoCivil;
	}
	public void setEstadoCivil(Long estadoCivil) {
		this.estadoCivil = estadoCivil;
	}
	public Long getAprobado() {
		return aprobado;
	}
	public void setAprobado(Long aprobado) {
		this.aprobado = aprobado;
	}
	
	
	
}
